package loopsandconditions;

public class IfElseConditon {
    public static void main(String[] args) {
        int x = 10, y = 12;
        if (x + y < 10){
            System.out.println("x+y is less than 10");
        }
        else {
            System.out.println("x+y is greater to 10");
        }
    }
}
